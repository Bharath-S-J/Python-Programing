# Math Functions in Python

# Rounding a number
x = 2.9
print(round(x))  # Output: 3 (rounds x to the nearest integer)

# Absolute value of a number
print(abs(-2.9))  # Output: 2.9 (returns the absolute value of -2.9)

# Using functions from the math module
import math

# Floor function (rounds down to the nearest integer)
print(math.floor(5.7))  # Output: 5 (5.7 rounded down to the nearest integer)

# Ceiling function (rounds up to the nearest integer)
print(math.ceil(5.7))   # Output: 6 (5.7 rounded up to the nearest integer)
